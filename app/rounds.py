"""Расписание раундов для таблиц от 4 до 8 игроков.

Каждый раунд — список пар (номера игроков 1-based).
Порядок раундов определяет рекомендуемую последовательность игр.
"""

from __future__ import annotations

# {размер_таблицы: [[пары_раунда_1], [пары_раунда_2], ...]}
ROUND_SCHEDULES: dict[int, list[list[tuple[int, int]]]] = {
    4: [
        [(1, 4), (2, 3)],
        [(1, 3), (2, 4)],
        [(1, 2), (3, 4)],
    ],
    5: [
        [(2, 5), (3, 4)],
        [(5, 1), (2, 3)],
        [(1, 4), (5, 3)],
        [(3, 1), (4, 2)],
        [(1, 2), (4, 5)],
    ],
    6: [
        [(1, 6), (2, 5), (3, 4)],
        [(5, 1), (6, 4), (2, 3)],
        [(1, 4), (5, 3), (6, 2)],
        [(3, 1), (4, 2), (5, 6)],
        [(1, 2), (3, 6), (4, 5)],
    ],
    7: [
        [(2, 7), (3, 6), (4, 5)],
        [(7, 1), (2, 5), (3, 4)],
        [(1, 6), (7, 5), (2, 3)],
        [(5, 1), (6, 4), (7, 3)],
        [(1, 4), (5, 3), (6, 2)],
        [(3, 1), (4, 2), (6, 7)],
        [(1, 2), (4, 7), (5, 6)],
    ],
    8: [
        [(1, 8), (2, 7), (3, 6), (4, 5)],
        [(7, 1), (8, 6), (2, 5), (3, 4)],
        [(1, 6), (7, 5), (8, 4), (2, 3)],
        [(5, 1), (6, 4), (7, 3), (8, 2)],
        [(1, 4), (5, 3), (6, 2), (7, 8)],
        [(3, 1), (4, 2), (5, 8), (6, 7)],
        [(1, 2), (3, 8), (4, 7), (5, 6)],
    ],
}


def get_round_schedule(table_size: int) -> list[list[tuple[int, int]]]:
    """Вернуть расписание раундов для таблицы заданного размера."""
    schedule = ROUND_SCHEDULES.get(table_size)
    if not schedule:
        raise ValueError(f"Нет расписания для таблицы на {table_size} игроков")
    return schedule


def get_match_round(table_size: int, p1: int, p2: int) -> int:
    """
    Определить номер раунда (0-based) для пары (p1, p2).

    Пара ищется в обоих направлениях — порядок в расписании может отличаться.
    """
    schedule = get_round_schedule(table_size)
    for round_idx, matches in enumerate(schedule):
        for a, b in matches:
            if {a, b} == {p1, p2}:
                return round_idx
    raise ValueError(f"Матч {p1}-{p2} не найден в расписании для {table_size} игроков")
