"""Варианты комбинаций таблиц в зависимости от количества игроков.

Правила:
- Минимум 4 игрока, максимум 12
- Таблица может содержать от 4 до 8 игроков
- Если вариант один — выбор пользователю не предлагается
- Таблицы в списке отсортированы по убыванию размера
"""

from __future__ import annotations

# {кол-во_игроков: [(описание, [размеры_таблиц]), ...]}
COMBINATIONS: dict[int, list[tuple[str, list[int]]]] = {
    4: [("1 таблица на 4", [4])],
    5: [("1 таблица на 5", [5])],
    6: [("1 таблица на 6", [6])],
    7: [("1 таблица на 7", [7])],
    8: [
        ("1 таблица на 8", [8]),
        ("2 таблицы по 4", [4, 4]),
    ],
    9: [("1×5 + 1×4", [5, 4])],
    10: [("2 таблицы по 5", [5, 5])],
    11: [("1×6 + 1×5", [6, 5])],
    12: [
        ("2 таблицы по 6", [6, 6]),
        ("3 таблицы по 4 (рек.)", [4, 4, 4]),
    ],
}

MIN_PLAYERS = 4
MAX_PLAYERS = 12


def get_combinations(player_count: int) -> list[tuple[str, list[int]]]:
    """Получить доступные варианты таблиц для данного кол-ва игроков."""
    if player_count < MIN_PLAYERS:
        raise ValueError(f"Минимум {MIN_PLAYERS} игрока для турнира")
    if player_count > MAX_PLAYERS:
        raise ValueError(f"Максимум {MAX_PLAYERS} игроков")
    return COMBINATIONS[player_count]
